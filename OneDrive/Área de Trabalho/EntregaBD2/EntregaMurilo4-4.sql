DROP TABLE PRODUTO;

CREATE TABLE PRODUTO(
    COD_PRO NUMBER PRIMARY KEY,
    VALOR NUMBER(10,2),
    COD_CAT CHAR(1)
);

CREATE TABLE ALIQUOTA (
    COD_CAT CHAR(1) PRIMARY KEY,
    IPI NUMBER(5,2)
);

